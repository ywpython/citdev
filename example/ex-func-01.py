# 실습문제 번외: 함수: n*n인 *로 그려진 정사각형

def draw_rect(n):
    for i in range(n):
        print('*'*n)

def draw_rect2(n):
    for i in range(n):
        if i == 0 or i == (n-1):
            print('*'*n)
        else:
            print('*'+ ' '*(n-2) + '*')

draw_rect2(1)
