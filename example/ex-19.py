money = int(input("현재 가진 금액 입력: "))

if money >= 20000:
    print("오늘 저녁은... 치킨이다.")
elif money >= 10000:
    print("오늘 저녁은... 떡복이다")
elif money >= 2000:
    print("오늘 저녁은... 편의점 김밥")
