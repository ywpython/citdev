# double for version
n = 5
for i in range(n):
    row = ''
    for j in range(n-i):
        row += '*'
    print(row)
#####################################################################        

# pythonic version
n = 5
for i in range(n):
    print('*'*(n-i))
#####################################################################        


for i in range(n, 0, -1):
    print("*" * i)
